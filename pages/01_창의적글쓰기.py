# =========================================================
# pages/01_창의적_글쓰기.py : 창의적 글쓰기 페이지
# - 사이드바의 이름과 학년은 인지과제 페이지와 함께 쓰입니다. (다시 입력하지 않아도 됨)
# - 10분 타이머가 끝나면 더 이상 글을 쓰거나 제출할 수 없습니다.
# =========================================================

import streamlit as st
import common                    # 두 페이지가 함께 쓰는 공통 기능
from tasks import 문제은행        # 기록 내려받기(CSV)를 만들 때 필요


# 1) 페이지 기본 설정 --------------------------------------
st.set_page_config(page_title="창의적 글쓰기", page_icon="✏️")

페이지키 = "창의적글쓰기"

# 2) 세션 준비 + 사이드바 학생 정보 ------------------------
#    - 인지과제 페이지에서 입력한 이름·학년이 그대로 이어집니다.
common.세션_준비()
이름, 학년 = common.학생정보_사이드바(페이지키)


# 3) 화면 제목 ---------------------------------------------
st.title("✏️ 창의적 글쓰기")
st.caption("정답이 없는 활동이에요. 자유롭게 상상해서 나만의 생각을 적어 보세요.")


# 4) 10분 타이머 (이 페이지만의 10분) ----------------------
남은초, 종료 = common.타이머(페이지키, 분=10)


st.divider()


# =========================================================
# 5) [첫 번째 칸] 글쓰기 칸
# =========================================================
st.header("📝 글쓰기 칸")

# 글쓰기 주제 (원하는 문장으로 바꿔서 쓰시면 됩니다)
글쓰기_주제 = "환경오염을 해결하기 위한 나만의 아이디어를 자유롭게 써 보세요."
st.info(f"**주제**\n\n{글쓰기_주제}")

# 글 입력칸 (시간이 끝나면 잠깁니다)
내글 = st.text_area(
    "나의 글",
    height=200,
    key=f"글_{페이지키}",
    placeholder="예) 학교에서 분리수거 게임을 만들어요!",
    disabled=종료,
)

# 제출 버튼
if st.button("📤 글 제출하기", disabled=종료):
    if 종료:
        st.error("시간이 끝나서 제출할 수 없어요.")
    elif not 이름:
        st.warning("먼저 사이드바에서 이름을 입력해 주세요.")
    elif not 내글.strip():
        st.warning("글을 입력해 주세요.")
    else:
        common.이름_저장공간_준비(이름, 학년)
        st.session_state["records"][이름]["글쓰기"].append(내글)
        st.success(f"'{이름}' 학생의 글이 제출되었어요! 👍")

# 지금까지 제출한 글 개수 보여 주기
if 이름 and 이름 in st.session_state["records"]:
    개수 = len(st.session_state["records"][이름]["글쓰기"])
    if 개수:
        st.caption(f"✅ 지금까지 제출한 글: {개수}편")


st.divider()


# =========================================================
# 6) [두 번째 칸] 생성형 AI 대화 칸
# =========================================================
st.header("🤖 생성형 AI 대화 칸")
st.caption("글을 쓰다가 막히면 AI 선생님과 이야기해 보세요.")

common.AI대화칸(
    페이지키=페이지키,
    이름=이름,
    학년=학년,
    문제_설명=f"[창의적 글쓰기 주제] {글쓰기_주제}",
)


# 7) 사이드바 - 내 기록 내려받기 ---------------------------
common.기록_내려받기_사이드바(이름, 문제은행)
