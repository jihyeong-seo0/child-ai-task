# =========================================================
# pages/01_창의적_글쓰기.py : 창의적 글쓰기 페이지
# - 사이드바의 이름·학년은 인지과제 페이지와 함께 쓰입니다. (다시 입력하지 않아도 됨)
# - 10분 타이머가 끝나면 더 이상 글을 쓰거나 제출할 수 없습니다.
# =========================================================

import streamlit as st
import common                    # 두 페이지가 함께 쓰는 공통 기능
from tasks import 문제은행        # 엑셀 기록을 만들 때 필요


# 1) 페이지 기본 설정 --------------------------------------
st.set_page_config(page_title="창의적 글쓰기", page_icon="✏️")

페이지키 = "창의적글쓰기"

# 2) 세션 준비 + 사이드바 학생 정보 ------------------------
#    - 인지과제 페이지에서 입력한 이름·학년이 그대로 이어집니다.
common.세션_준비()
이름, 학년 = common.학생정보_사이드바(페이지키)
common.초기화_버튼(페이지키)   # 🧹 현재 작업 초기화 (저장된 기록은 남습니다)

버전 = st.session_state["_ver"]   # 초기화하면 올라가는 번호 (입력칸 비우기에 사용)


# 3) 화면 머리글 (날짜 + 제목) -----------------------------
#    - 인지과제 페이지와 똑같은 모양으로 나옵니다.
common.페이지_머리글(
    "✏️", "창의적 글쓰기",
    "환경오염을 해결하기 위한 아이디어를 자유롭게 작성해 주세요.",
)


# 4) 10분 타이머 (이 페이지만의 10분) ----------------------
#    - '시작'을 누르면 10분이 흘러갑니다.
#    - 시간이 끝나면 종료=True 가 되어 글을 쓸 수 없게 잠깁니다.
남은초, 종료 = common.타이머(페이지키, 분=10)


st.divider()


# =========================================================
# 5) [첫 번째 칸] 아이디어 작성 칸
# =========================================================
st.header("✏️ 아이디어 작성 칸")
st.caption("환경오염을 해결할 나만의 아이디어를 자유롭게 적어 보세요.")

내글 = st.text_area(
    "나의 아이디어",
    height=180,
    key=f"글_{페이지키}_{버전}",
    placeholder="예) 학교에서 분리수거 게임을 만들어요!",
    disabled=종료,
)

# 아이디어 제출 버튼 (시간이 끝나면 잠깁니다)
if st.button("📤 아이디어 제출하기", disabled=종료):
    if 종료:
        st.error("시간이 끝나서 제출할 수 없어요.")
    elif not 이름:
        st.warning("먼저 사이드바에서 이름을 입력해 주세요.")
    elif not 내글.strip():
        st.warning("아이디어 내용을 입력해 주세요.")
    else:
        common.이름_저장공간_준비(이름, 학년)
        st.session_state["records"][이름]["글쓰기"].append(내글)
        st.success(f"'{이름}' 학생의 아이디어가 제출되었어요! 👍")

# 지금까지 제출한 아이디어 개수
if 이름 and 이름 in st.session_state["records"]:
    개수 = len(st.session_state["records"][이름]["글쓰기"])
    if 개수:
        st.caption(f"✅ 지금까지 제출한 아이디어: {개수}개")


st.divider()


# =========================================================
# 6) [두 번째 칸] 생성형 AI 대화 칸
# =========================================================
st.header("🤖 생성형 AI 대화 칸")
st.caption("아이디어를 떠올리다 막히면 AI 선생님과 이야기해 보세요.")

common.AI대화칸(
    페이지키=페이지키,
    이름=이름,
    학년=학년,
    문제_설명="[창의적 글쓰기 주제] 환경오염을 해결하기 위한 아이디어를 작성해주세요.",
)


# =========================================================
# 7) 사이드바 - 연구자 패널
#    - 암호 '0000'을 입력해야 제출한 아이디어를 보거나 엑셀로 내려받을 수 있습니다.
# =========================================================
암호맞음 = common.연구자_패널(페이지키, 문제은행)

# 암호를 맞게 입력했을 때만 제출한 아이디어를 화면에서 볼 수 있어요.
if 암호맞음 and 이름 and 이름 in st.session_state["records"]:
    제출목록 = st.session_state["records"][이름]["글쓰기"]
    if 제출목록:
        with st.expander(f"📌 '{이름}' 학생이 제출한 아이디어 보기 ({len(제출목록)}개)"):
            for 번호, 글 in enumerate(제출목록, start=1):
                st.write(f"{번호}. {글}")
