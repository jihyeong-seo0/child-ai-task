# (2) 스트리밍으로 받은 글자를 실시간으로 흘려보내 주는 도우미 함수
def 실시간_글자흐름(스트림):
    """AI가 보내주는 조각(chunk)에서 글자만 뽑아 하나씩 내보냅니다."""
    for 조각 in 스트림:
        내용 = 조각.choices[0].delta.content
        if 내용:
            yield 내용


# (3) 화면 아래쪽 채팅 입력창
질문 = st.chat_input("메시지를 입력하세요")

