# 13) 기록을 표(CSV)로 만들어 주는 함수
#     - 이름을 기준으로 [정답] -> [AI 대화] 순서로 정리됩니다.
# =========================================================
def 기록_csv만들기(대상기록):
    저장통 = io.StringIO()
    쓰기 = csv.writer(저장통)
    쓰기.writerow(["이름", "학년", "구분", "번호", "내용(문제/프롬프트)", "학생 답/AI 답변"])

    for 이름, 정보 in 대상기록.items():
        문제들 = 문제은행[정보["학년"]]
        # (1) 인지과제 정답 부분
        for 문제번호 in sorted(정보["정답"].keys()):
            쓰기.writerow([
                이름, 정보["학년"], "인지과제", 문제번호 + 1,
                문제들[문제번호]["문제"].replace("\n", " "),
                정보["정답"][문제번호],
            ])
        # (2) AI 대화 부분
        for i, 쌍 in enumerate(정보["대화"], start=1):
            쓰기.writerow([이름, 정보["학년"], "AI 대화", i, 쌍["프롬프트"], 쌍["결과물"]])

    # 엑셀에서 한글이 깨지지 않도록 BOM(\ufeff)을 앞에 붙여 줍니다.
    return ("\ufeff" + 저장통.getvalue()).encode("utf-8")


